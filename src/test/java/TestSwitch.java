public class TestSwitch {
    public static void main(String[] args) {
        int n =1;
        switch (n){
            case 1:
                System.out.println("实例1");
                break;
            case 2 :
                System.out.println("实例2");
                break;
                default:
                    System.out.println("默认语句");
                    break;
        }
        while(n!=1){
            System.out.println("fate/grand order");
        }
    }
}
